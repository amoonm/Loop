public class Main {
    public static void main(String[] args) {
   
        //for loop to print numbers from 0-100
	for (int i = 0; i <= 100; i++) {
	   //if statement to get even numbers between 0-100
	   if (i % 2 == 0) {
		System.out.println(i + " ");
	   }
	}
    }
}
